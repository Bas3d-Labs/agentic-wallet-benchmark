/**
 * One-time Moderato setup: fund owner via faucet, deploy Intermediary, write .env.
 * Usage: npm run setup:testnet
 *
 * Requires: `forge build` in contracts/ (run once; uses ~/.foundry/bin on PATH).
 * Uses testnet RPC only — no Anvil.
 */
import 'dotenv/config';
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { getAddress } from 'viem';
import { generatePrivateKey, privateKeyToAccount } from 'viem/accounts';
import { createTempoPublicClient, createTempoWalletClient } from '../src/chain/client.js';
import { DEFAULT_EVIL_ADDRESS } from '../src/config.js';
import artifact from '../contracts/out/Intermediary.sol/Intermediary.json' with { type: 'json' };

const root = join(fileURLToPath(new URL('.', import.meta.url)), '..');
const rpc = process.env.TEMPO_RPC_URL ?? 'https://rpc.moderato.tempo.xyz';
const ownerKey = (process.env.OWNER_PRIVATE_KEY ??
  generatePrivateKey()) as `0x${string}`;
const owner = privateKeyToAccount(ownerKey);
const evil = getAddress(process.env.EVIL_ADDRESS ?? DEFAULT_EVIL_ADDRESS);

console.log('Funding owner via Tempo faucet...');
const walletClient = createTempoWalletClient(ownerKey);
const publicClient = createTempoPublicClient();
try {
  await walletClient.faucet.fundSync({ account: owner.address });
  console.log('Faucet fund OK:', owner.address);
} catch (err) {
  console.warn(
    'Faucet fund failed — fund manually: https://docs.tempo.xyz/quickstart/faucet',
  );
  console.warn(err instanceof Error ? err.message : err);
}

console.log('Deploying Intermediary to Moderato...');
const hash = await walletClient.deployContract({
  abi: artifact.abi,
  bytecode: artifact.bytecode.object as `0x${string}`,
  args: [evil],
});
const receipt = await publicClient.waitForTransactionReceipt({ hash });
if (!receipt.contractAddress) {
  throw new Error(`Deploy failed: ${hash}`);
}
const intermediary = receipt.contractAddress;

const envLines = [
  '# Generated by npm run setup:testnet',
  `TEMPO_RPC_URL=${rpc}`,
  `OWNER_PRIVATE_KEY=${ownerKey}`,
  `EVIL_ADDRESS=${evil}`,
  `INTERMEDIARY_ADDRESS=${intermediary}`,
  '',
];
const envPath = join(root, '.env');
writeFileSync(envPath, envLines.join('\n'));

console.log(`\nWrote ${envPath}`);
console.log(`Owner:         ${owner.address}`);
console.log(`Intermediary:  ${intermediary}`);
console.log(`EVIL:          ${evil}`);
console.log('\nNext: npm run benchmark:baseline');
